void main() {
  int num1 = 10;
  int num2 = 3;

  // Calculos aritmeticos
  int suma = num1 + num2;
  int resta = num1 - num2;
  int unarioNeg = -num1;
  int mult = num1 * num2;
  double div = num1 / num2;
  int div2 = num1 ~/ num2;
  int mod = num1 % num2;

  // Impresion de resultados
  print("La suma es $suma");
  print("La resta es $resta");
  print("El unario negativo es $unarioNeg");
  print("La multiplicacion es $mult");
  print("La division es $div");
  print("La division entera es $div2");
  print("El modulo es $mod");
}