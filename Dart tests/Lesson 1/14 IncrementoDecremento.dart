void main() {
  int num1 = 0;
  int num2 = 0;

  // Operaciones incremento y decremento

  // Pre incremento
  num2 = ++num1;
  print("El valor de num2 es $num2");

  // Reset de los valores a 0
  num1 = 0;
  num2 = 0;

  // Post incremento
  num2 = num1++;
  print("El valor de num2 es $num2");
}