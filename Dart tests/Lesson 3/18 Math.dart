import 'dart:math';
void main(){
  int num1 = 10;
  int num2 = 2;

  num powernum = pow(num1,num2);
  num maxnum = max(num1,num2);
  num minnum = min(num1,num2);
  num squareroot = sqrt(25); 
  
  print("La potencia es $powernum"); 
  print("El maximo es $maxnum"); 
  print("El minimo es $minnum"); 
  print("La raiz cuadrada es $squareroot"); 
}